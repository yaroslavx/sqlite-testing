export const arrTo3DArray = (arr: number[]) => {
  const resArr: number[][][] = [
    [[], [], [], [], []],
    [[], [], [], [], []],
    [[], [], [], [], []],
    [[], [], [], [], []],
  ];
  let i = 0;
  for (let x = 0; x < 4; x++) {
    for (let y = 0; y < 5; y++) {
      for (let z = 0; z < 4; z++) {
        resArr[x][y].push(arr[i++]);
      }
    }
  }
  return resArr;
};
